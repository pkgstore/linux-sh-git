#!/usr/bin/bash

(( EUID == 0 )) &&
  { echo >&2 "This script should not be run as root!"; exit 1; }

# -------------------------------------------------------------------------------------------------------------------- #
# Get options.
# -------------------------------------------------------------------------------------------------------------------- #

curl=$( which curl )
sleep="2"

OPTIND=1

while getopts "t:n:o:h" opt; do
  case ${opt} in
    t)
      token="${OPTARG}"
      ;;
    n)
      project_name="${OPTARG}"; IFS=';' read -ra project_name <<< "${project_name}"
      ;;
    o)
      project_org="${OPTARG}"
      ;;
    h|*)
      echo "-t [token] -n [project_name] -o [project_org]"
      exit 2
      ;;
  esac
done

shift "$(( OPTIND - 1 ))"

(( ! ${#project_name[@]} )) && exit 1

# -------------------------------------------------------------------------------------------------------------------- #
# -----------------------------------------------------< SCRIPT >----------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

for i in "${project_name[@]}"; do
  echo "" && echo "--- Open: ${i}"

  ${curl}                                       \
  -X DELETE                                     \
  -H "Authorization: token ${token}"            \
  -H "Accept: application/vnd.github.v3+json"   \
  "https://api.github.com/repos/${project_org}/${i}"

  echo "" && echo "--- Done: ${i}" && echo ""

  sleep ${sleep}
done

# -------------------------------------------------------------------------------------------------------------------- #
# Exit.
# -------------------------------------------------------------------------------------------------------------------- #

exit 0
